public class Triangle_pattern2
{
    public static void main(String[] args)
    {
        int i;
        for(i=5;i>=1;i--)
        {
            int j;
            for (j=i;j>=1;j--)
            {
                System.out.print( j+" ");
            }
            System.out.println();
        }
        
    }
}